template <class T>
bool isOK(const vector<T> &a, size_t index, T key)
{
    if (a[index] >= key)
        return true;
    else
        return false;
}
template <class T>
int binary_search(const vector<T> &a, T key)
{
    size_t left = 0;
    size_t right = a.size();

    while (right - left > 1)
    {
        size_t mid = left + (right - left) / 2;

        if (isOK(a, mid, key))
            right = mid;
        else
            left = mid;
    }

    return right;
}
using Graph = vector<vector<int>>;
vector<bool> seen;
void dfs(const Graph &g, int v)
{
    seen[v] = true; // v を訪問済にする

    // v から行ける各頂点 next_v について
    for (auto next_v : g[v])
    {
        if (seen[next_v])
            continue;   // next_v が探索済だったらスルー
        dfs(g, next_v); // 再帰的に探索
    }
}
